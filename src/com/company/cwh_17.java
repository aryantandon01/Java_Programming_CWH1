package com.company;

public class cwh_17 {
    public static void main(String[] args) {
        //Logical Operators
        //AND
        System.out.println("For logical AND:");

        boolean a = true;
        boolean b = true;
        boolean c = true;

        if (a && b && c)
        {
            System.out.println("Y");
        }
        else
        {
            System.out.println("N");
        }

        //OR
        System.out.println("For logical OR:");

        if (a || b || c)
        {
            System.out.println("Y");
        }
        else
        {
            System.out.println("N");
        }

    }
}
