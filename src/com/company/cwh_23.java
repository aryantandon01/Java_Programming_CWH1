package com.company;
import java.util.Scanner;

public class cwh_23 {
    public static void main(String[] args) {
        //Odd numbers
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the value of n: ");
        int n = sc.nextInt();
        for (int i = n; i > 0; i--)
        {
            System.out.println(i);
        }
    }
}
