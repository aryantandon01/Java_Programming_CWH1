package com.company;

public class cwh_21 {
    public static void main(String[] args) {
        int i, n = 0, m = 0;

        for (i = 0; i < 3; i++)
        {
            System.out.println(i+1);
        }

        while (n < 3)
        {
            System.out.println(n+1);
            n++;
        }

        do
        {
            System.out.println(m+1);
            m++;
        } while (m < 3);

    }
}
